class Libro():

    def __init__(self, isbn, autor, año, titulo, edicion, temas, nocopias, estanteria, fila):
        self.isbn = isbn
        self.autor = autor
        self.año = año
        self.titulo = titulo
        self.edicion = edicion
        self.temas = temas
        self.nocopias = nocopias
        self.estanteria = estanteria
        self.fila = fila